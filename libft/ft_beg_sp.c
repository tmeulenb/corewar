#include "libft.h"

int	ft_beg_sp(char const *str)
{
	int	i;

	i = 0;
	while ((str[i] == ' ' || str[i] == '\n' || str[i] == '\t') \
		&& str[i] != '\0')
	{
		i++;
	}
	return (i);
}
