#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	str = (char *)malloc(sizeof(char) * size + 1);
	if (!str)
		return (NULL);
	str = ft_memset((void *)str, '\0', sizeof(char) * size + 1);
	return (str);
}
